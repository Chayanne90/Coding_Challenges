package TutorialsChallenges;

public class multidensionArray {

    public static void main(String[] args) {

        int firstArr [][] = {{50,4,6,5,80},
                             {65,9,7,65,12}};

        int secondArr [][] = {{65,69,85,2,0},
                              {100,55,10,5,8}};

        System.out.println(firstArr[0][2]);
        System.out.println(firstArr[1][4]);

        System.out.println(secondArr[0][2]);
        System.out.println(secondArr[1][0]);

    }
}
